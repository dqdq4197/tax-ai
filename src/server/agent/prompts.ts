export function buildSystemPrompt(): string {
  const today = new Date().toLocaleDateString("ko-KR", {
    year: "numeric",
    month: "long",
    day: "numeric",
    timeZone: "Asia/Seoul",
  });
  return systemPromptTemplate.replace("{{TODAY}}", today);
}

